// Define a function that takes two arguments 
// and returns the minimum 

function minimum(number, comparison) {
  return number > comparison ? comparison : number; 
}

console.log(minimum(200, 5));
console.log(minimum(2324, 3245));
console.log(minimum(-1, -6));

